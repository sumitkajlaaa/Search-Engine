package com.Accio;

public class SearchResults {
    String title;
    String link;

    public SearchResults() {
        this.title = null;
        this.link = null;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
